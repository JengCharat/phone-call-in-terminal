import numpy as np

# ข้อความที่ให้มา
x = "[[ 6.2364863e-10  7.4397349e-10] [ 3.6275612e-08  4.3308404e-08]] [-8.7975373e-04 -7.9718593e-04] [-9.0596342e-04 -8.3841407e-04]]"

# ลบวงเล็บที่อยู่รอบๆ ข้อความ
x = x[1:-1]  # ลบวงเล็บทั้งคู่ออก

# แยกข้อมูลด้วย "] [" และลบช่องว่างที่ไม่จำเป็น
x = x.split("] [")  # แยกข้อมูลด้วย "] ["

# ลบวงเล็บในแต่ละส่วน แล้วแปลง string เป็น float
x = [list(map(float, item.replace("[", "").replace("]", "").split())) for item in x]

# แปลงเป็น numpy array
array = np.array(x)

# แสดงผล
print(array)
